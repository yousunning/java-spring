package relational;

public class oprerationEx4 {

	public static void main(String[] args) {
		// 관계연산자
		int num1 = 5;
		int num2 =3;
		boolean value = (num1 > num2);
		
		System.out.println(value);        //t
		System.out.println(num1 < num2);  //f
		System.out.println(num1 >= num2); //t
		System.out.println(num1 <= num2); //f
		System.out.println(num1 != num2); //t
		System.out.println(num1 == num2); //f
	}

}
