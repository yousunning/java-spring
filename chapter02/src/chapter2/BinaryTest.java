package chapter2;

public class BinaryTest {

	public static void main(String[] args) {
		// 리터럴 표현 그리고 한줄설명
		int num = 10; // 10진수
		int bNum = 0B1010; // 2진수 0B
		int oNum = 012; // 8진수 0
		int hNum = 0XA; // 16진수 0X 리터럴 표현
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}

}
