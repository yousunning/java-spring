package loopexample;

public class BasicLoop {

	public static void main(String[] args) {
		// 1부터 10까지의 합을 출력하시오 / 회차변수 : num
		int sum = 1;
		sum += 2; // sum = sum + 2; 와 같다 // 3
		sum += 3;
		sum += 4;
		sum += 5;
		sum += 6;
		sum += 7;
		sum += 8;
		sum += 9;
		sum += 10;
		
		System.out.println("1부터 10까지의 합은 " + sum + " 입니다.");

		
	}

}
